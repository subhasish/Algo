#include <bits/stdc++.h>
using namespace std;

int main()
{
	cout << abs(-24) << endl;
	cout << fabs(-2.14) << endl;
	cout << ceil(2.4) << endl;
	cout << ceil(-2.4) << endl;
	cout << floor(2.4) << endl;
	cout << floor(-2.4) << endl;
	cout << min(-2,4) << endl;
	cout << max(-2,4) << endl;
	cout << pow(-2,-4) << endl;
	cout << log(24) << endl;
	cout << sqrt(24) << endl;
	cout << isnan(log(-24)) << endl;
	cout << isnan(sqrt(-24)) << endl;
}

