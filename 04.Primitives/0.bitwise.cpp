#include <bits/stdc++.h>
using namespace std;

int main()
{
	// bitwise operators
	cout << (6&4) <<endl;
	cout << (1|2) <<endl;
	cout << (8>>1) <<endl;
	cout << (1<<10) <<endl;
	cout << (~0) <<endl;
	cout << (~0>>1) <<endl;
	cout << (15^1) <<endl;
}


